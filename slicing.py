#Extracting more than one values from list,tuuple or np.array
names = ["Ammad", "Naeem", "Khan", "Durrani",5,6,7,8,9,10]
print(names[1:4:1])
print(names[-3:-1:1])
print(names[2:])
print(names[-2:])
print(names[::1])
print(names[::3])
print(names[6:1:1])
print(names[-8:8:])
print(names[-5:-9:1])
print(names[::-1])
